package thirty1stbatch;

public class Assgn10ForLoop {

	public static void main(String[] args) {
		for(int i=1; i<=3; i++)  //for rows
		{
			for(int j=1; j<=4; j++)  // for columns
			{
				System.out.print("*");
			}
			System.out.println();
			System.out.println();
			// to enter into next row
		}

	}

}
